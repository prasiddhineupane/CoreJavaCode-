package AssignmentJuly24;
// sum of even and odd numbers between 1 t0 20.
public class sumEvenandOddDemo {
    public static void main(String[] args) {
        int even = 0;
        int odd = 0;

        for (int i = 1; i <= 20; i++) {
            if (i % 2 == 0) {
                even += i; // Add even numbers to sum
            } else {
                odd += i;  // Add odd numbers to sum
            }
        }

        System.out.println("Sum of even numbers between 1 and 20: " + even);
        System.out.println("Sum of odd numbers between 1 and 20: " + odd);
    }
}
